def area(a, h):
    """
    Функция принимает два знаечния: a - длина основания треугольника,
    h - длина высоты треугольнка, проведённая к а.
    Возвращает площадь треугольника, вычисленную по формуле (a * h)/2.
    """
    return a * h / 2 

def perimeter(a, b, c):
    """
    Функция принимает 3 параметра: a, b, c - длины сторон треугольника. Вычисляет значение периметра по формуле a + b + c.
    Возвращает значение периметра.
    """
    return a + b + c 
